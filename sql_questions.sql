SELECT
	CUSTOMER_NAME,
	SUM(PRICE * QUANTITY) AS TOTAL_COST
FROM
	ORDERS_1
WHERE
	CATEGORY = 'Electronics'
GROUP BY
	CUSTOMER_NAME
HAVING
	SUM(PRICE * QUANTITY) > 1000
ORDER BY
	TOTAL_COST


